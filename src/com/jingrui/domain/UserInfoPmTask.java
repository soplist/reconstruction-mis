package com.jingrui.domain;

public class UserInfoPmTask {
    private String name;
    private Float selfValue;
    private Float departmentValue;
    private Float managerValue;
    private Float companyValue;
    private Float staffValue;
    public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Float getSelfValue() {
		return selfValue;
	}
	public void setSelfValue(Float selfValue) {
		this.selfValue = selfValue;
	}
	public Float getDepartmentValue() {
		return departmentValue;
	}
	public void setDepartmentValue(Float departmentValue) {
		this.departmentValue = departmentValue;
	}
	public Float getManagerValue() {
		return managerValue;
	}
	public void setManagerValue(Float managerValue) {
		this.managerValue = managerValue;
	}
	public Float getCompanyValue() {
		return companyValue;
	}
	public void setCompanyValue(Float companyValue) {
		this.companyValue = companyValue;
	}
	public Float getStaffValue() {
		return staffValue;
	}
	public void setStaffValue(Float staffValue) {
		this.staffValue = staffValue;
	}
	
}
