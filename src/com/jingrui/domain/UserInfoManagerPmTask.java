package com.jingrui.domain;

public class UserInfoManagerPmTask {
    private String name;
    private Float leaderValue;
    private Float selfValue;
    private Float staffValue;
    
    public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Float getLeaderValue() {
		return leaderValue;
	}
	public void setLeaderValue(Float leaderValue) {
		this.leaderValue = leaderValue;
	}
	public Float getSelfValue() {
		return selfValue;
	}
	public void setSelfValue(Float selfValue) {
		this.selfValue = selfValue;
	}
	public Float getStaffValue() {
		return staffValue;
	}
	public void setStaffValue(Float staffValue) {
		this.staffValue = staffValue;
	}
	
}
